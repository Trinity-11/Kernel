; LPT basic interface register

LPT_BASE_ADDRESS = $AF1378  ;

;--------------
PPT_DATA		    = LPT_BASE_ADDRESS + $00  ; AF1378
PPT_STATUS		  = LPT_BASE_ADDRESS + $01  ; AF1379
PPT_CONTROL		  = LPT_BASE_ADDRESS + $02  ; AF137A
PPT_EPP_ADDR    = LPT_BASE_ADDRESS + $03  ; AF137B
EPP_DATA_PORT_0 = LPT_BASE_ADDRESS + $04  ; AF137C
EPP_DATA_PORT_1 = LPT_BASE_ADDRESS + $05  ; AF137D
EPP_DATA_PORT_2 = LPT_BASE_ADDRESS + $06  ; AF137E
EPP_DATA_PORT_3 = LPT_BASE_ADDRESS + $07  ; AF137F

;--------------
ECP_DATA        = LPT_BASE_ADDRESS + $00  ; AF1378
ECP_A_FIFO      = LPT_BASE_ADDRESS + $00  ; AF1378
ECP_DSR         = LPT_BASE_ADDRESS + $01  ; AF1379
ECP_DCR         = LPT_BASE_ADDRESS + $02  ; AF137A

ECP_FIFO        = LPT_BASE_ADDRESS + $400  ; AF1778
ECP_D_FIFO      = LPT_BASE_ADDRESS + $400  ; AF1778
ECP_T_FIFO      = LPT_BASE_ADDRESS + $400  ; AF1778
ECP_A_CONF      = LPT_BASE_ADDRESS + $400  ; AF1778

ECP_B_CONF      = LPT_BASE_ADDRESS + $401  ; AF1779
ECP_ECR         = LPT_BASE_ADDRESS + $402  ; AF177A   use to sellect the LPT mode

; writing 3A at address will set the LPT module in the ECP mode
LPT_SET_ECP_MODE_CMD = $3A
LPT_SET_ECP_MODE_ADRESS = $F0
